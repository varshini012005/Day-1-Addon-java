package basic.concept;

public class DataTypes {

	public static void main(String[] args) {
		//Primitive Data types
		//1.number type
		byte a = 127;
		short b = 32_767;
		int c = 2_147_483_647;
		long d = 9_223_372_036_854_775_007L; 
		float e = 1.123456789f;
		double f = 1.12345678910111245;
		boolean status = true;
		char initial = 'R';
		System.out.println("representing:"+ a);
		System.out.println("representing:"+ b);
		System.out.println("representing:"+ c);
		System.out.println("representing:" + d);
		System.out.println("representing:" + e);
		System.out.println("representing:" + status);
		System.out.println("representing:" + initial);
		System.out.println("representing:" + f);

	}

}
